bool GL_InitCoreProfile(const char *Driver)
{
    if ( Driver == NULL ) {
        DPrintf("NULL Driver Name...Aborting.\n");
        return false;
    }

    VidConf.GLLibHandle = (void*)(long)((SDL_GL_LoadLibrary(Driver) == -1) ? 0 : 1);

    if ( !VidConf.GLLibHandle ) {
        char fn[1024];
        getcwd(fn, sizeof(fn));
        strcat(fn,"/");
        strcat(fn,Driver);
        VidConf.GLLibHandle = (void*)(long)((SDL_GL_LoadLibrary(fn) == -1) ? 0 : 1);
        
        if ( !VidConf.GLLibHandle ) {
            DPrintf("Couldn't Open %s\n", fn);
        }
        return false;
    }

    if ( !GL_LookUpSym() ) {
        DPrintf("Something went wrong...\n");
        return false;
    }
    return true;
}

